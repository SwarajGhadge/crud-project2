const mongoose = require("mongoose");

const usersList = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  phoneNumber: {
    type: Number,
    required: true,
    unique: true,
  },
});
mongoose.models = {};
module.exports = mongoose.model("User", usersList);
